#include "libft.h"

char	*ft_strdup(char *src)
{
	char	*new;
	int		i;

	new = malloc (sizeof(char) * ft_strlen(src) + 1);
	if (new == NULL)
		return (NULL);
	i = 0;
	while (src[i])
	{
		new[i] = src[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
